package com.github.nickvl.xspring.core.log.aop;

/**
 * Log level severity enumeration in acceding order.
 */
public enum LogLevel {
    /**
     * Trace logging.
     */
    TRACE,

    /**
     * Debug logging.
     */
    DEBUG,

    /**
     * Info logging.
     */
    INFO,

    /**
     * Warn logging.
     */
    WARN,

    /**
     * Error logging.
     */
    ERROR,

    /**
     * Fatal logging.
     */
    FATAL
}
